    private void txt_accountKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_accountKeyReleased
        // TODO add your handling code here:
        String acc_regax="^[0-9]{16}$";
        Pattern pat=Pattern.compile(acc_regax);
        Matcher match=pat.matcher(txt_account.getText());
        if(!match.matches())
        {
            clab1.setText("Please Enter Valid Card Number");
        }
        else
        {
            clab1.setText(" ");
        }
    }//GEN-LAST:event_txt_accountKeyReleased

    private void txt_name_cardKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_name_cardKeyReleased
        // TODO add your handling code here:
        String fname_regax="^[A-Za-z]{3,20}$";
        Pattern pat=Pattern.compile(fname_regax);
        Matcher match=pat.matcher(txt_name_card.getText());
        if(!match.matches())
        {
            nlab.setText("Please Enter Valid Card Name");
        }
        else
        {
            nlab.setText(" ");
        }
    }//GEN-LAST:event_txt_name_cardKeyReleased

    private void btn_regActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_regActionPerformed
        // TODO add your handling code here:
        String q1 = "select * from customer where person_id = " + Login_Form.special_person_id;
        try {
            Connection con = MysqlConnect.ConnectDb();
            PreparedStatement ps = con.prepareStatement(q1);
            ResultSet rs = ps.executeQuery();
            if(rs.next() != false ) {
                JOptionPane.showMessageDialog(null, "You already have registered as a Customer");
            }
            else {
                if(Login_Form.special_account_type.equals("Customer")) {
                    String sql = "insert into customer (person_id, name_on_card, card_no, payment_type) values (" + x + ",'"+ txt_name_card.getText() + "'," + txt_account.getText() + ",'" + txt_payment.getText() + "')";

                    try {
                        Connection conn = MysqlConnect.ConnectDb();
                        Statement st = conn.createStatement();
                        st.executeUpdate(sql);
                        JOptionPane.showMessageDialog(null, "Registration Successful");
                    }
                    catch(SQLException ex) {
                        System.out.println("Insert Error" + ex);
                    }

                    //this.setVisible(false);
                    //new Login_Form().setVisible(true);
                }
                else {
                    JOptionPane.showMessageDialog(null, "You have not signed up as Customer", "Access Denied", JOptionPane.ERROR_MESSAGE);

                }

            }
        }
        catch (SQLException e) {
            // TODO Auto-generated catch block
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_regActionPerformed

